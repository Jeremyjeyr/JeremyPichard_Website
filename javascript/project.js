/* Fabstore */

$(document).ready(function () {
    $('#fabstore_project .slash').click(function () {
        $(this).toggleClass('open');
        $('#fabstore_project .hidden_content').toggleClass('open');
        $('#fabstore_project').toggleClass('open');
        $('#fabstore_project h3').toggleClass('open');

    });
});
$(document).ready(function () {
    $('#fabstore_project h3').click(function () {
        $('#fabstore_project .slash').addClass('open');
        $('#fabstore_project .hidden_content').addClass('open');
        $('#fabstore_project').addClass('open');
        $(this).addClass('open');

    });
});

